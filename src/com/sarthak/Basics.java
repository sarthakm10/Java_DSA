package com.sarthak;

public class Basics {
    public static void main(String[] args) {
        int a = 10;
        if (a == 10) {
            System.out.println("Hello World");
        }

        // while loop
        int count = 1;
        while(count != 5) {
            System.out.println(count);
            count++;
        }

        // for loop
        for(int num = 1; num != 5; num++) {
            System.out.println(num);
        }
    }
}
